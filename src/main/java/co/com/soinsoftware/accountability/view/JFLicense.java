	private boolean validateLicense() {
		boolean isValid = true;
		final String license = this.jtfLicense.getText();
		// final String encLicense =
		// this.controller.encryptLicenseData(license);
		if (license.equals("")) {
			isValid = false;
			ViewUtils.showMessage(this, MSG_LICENSE_REQUIRED,
					ViewUtils.TITLE_REQUIRED_FIELDS, JOptionPane.ERROR_MESSAGE);
		} else if (!this.controller.isValidLicense(license)) {
			isValid = false;
			ViewUtils.showMessage(this, MSG_INVALID_LICENSE_REQUIRED,
					ViewUtils.TITLE_REQUIRED_FIELDS, JOptionPane.ERROR_MESSAGE);
		}
		return isValid;
	}

